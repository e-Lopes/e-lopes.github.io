CREATE OR REPLACE VIEW "public"."v_monthly_ranking" AS
 SELECT "date_trunc"('month'::"text", ("tr"."tournament_date")::timestamp with time zone) AS "month",
    "p"."name" AS "player",
    "sum"(
        CASE "tr"."placement"
            WHEN 1 THEN 4
            WHEN 2 THEN 3
            WHEN 3 THEN 2
            WHEN 4 THEN 1
            ELSE NULL::integer
        END) AS "points"
   FROM ("public"."tournament_results" "tr"
     JOIN "public"."players" "p" ON (("tr"."player_id" = "p"."id")))
  GROUP BY ("date_trunc"('month'::"text", ("tr"."tournament_date")::timestamp with time zone)), "p"."name"
  ORDER BY ("date_trunc"('month'::"text", ("tr"."tournament_date")::timestamp with time zone)) DESC, ("sum"(
        CASE "tr"."placement"
            WHEN 1 THEN 4
            WHEN 2 THEN 3
            WHEN 3 THEN 2
            WHEN 4 THEN 1
            ELSE NULL::integer
        END)) DESC;


