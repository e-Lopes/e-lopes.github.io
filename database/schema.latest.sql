            WHEN ("tr"."placement" = 1) THEN 1
            ELSE 0
        END) AS "titles"
   FROM ("public"."tournament_results" "tr"
     JOIN "public"."decks" "d" ON (("tr"."deck_id" = "d"."id")))
  GROUP BY "d"."name"
  ORDER BY ("sum"(
        CASE
            WHEN ("tr"."placement" = 1) THEN 1
            ELSE 0
        END)) DESC;


