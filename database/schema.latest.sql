CREATE OR REPLACE VIEW "public"."v_deck_representation" AS
 SELECT "d"."name" AS "deck",
    "count"(*) AS "appearances"
   FROM ("public"."tournament_results" "tr"
     JOIN "public"."decks" "d" ON (("d"."id" = "tr"."deck_id")))
  GROUP BY "d"."name"
  ORDER BY ("count"(*)) DESC;


